char	*ft_strchr(const char *s, int c)
{
	char	*p;

	if (c > 255)
		c -= 256;
	while (*s)
	{
		if (*s == c)
		{
			p = (char *)s;
			return (p);
		}
		s++;
	}
	if (c == 0)
	{
		p = (char *)s;
		return (p);
	}
	return (0);
}
